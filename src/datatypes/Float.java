package datatypes;

public class Float {

	public static void main(String[] args) {
		
		//decimal values
		//myfloat value(32 bit)
		float myfloat=32.56123421f;
		System.out.println("myfloat value is:"+myfloat);
		//doublefloat(64)
		double doublefloat=3662.56123421144464;
		System.out.println("my doublefloat is:"+doublefloat);
		
		
		//boolean operation is nothing but a the operation is either true or false operation
		
		boolean mystatus=true;
		System.out.println(mystatus);
		
		
		boolean mystatus1=false;
		System.out.println(mystatus1);
		
		
		//char(16bit)
		char mygender ='M';
		System.out.println("my gender is:"+mygender);
		
		
		
		
		
		
		}

}
