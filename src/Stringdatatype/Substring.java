package Stringdatatype;

public class Substring {

	public static void main(String[] args) {
	String str = "mende poojitha";
System.out.println(str.substring(6));
System.out.println(str.substring(0, 14));

String strd = "chekka neeha";
System.out.println(strd.substring(7));
System.out.println(strd.substring(0, 6));
System.out.println(strd.substring(0, 12));

String sdt = "chekka hithika";
System.out.println(sdt.substring(7));
System.out.println(sdt.substring(0, 6));
System.out.println(sdt.substring(0, 14));


String asp = "chekka santhosh";
System.out.println(asp.substring(7));
System.out.println(asp.substring(0, 6));
System.out.println(asp.substring(0, 15));
	
	}

}
