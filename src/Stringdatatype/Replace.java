package Stringdatatype;

public class Replace {

	public static void main(String[] args) {
		String str = "mindqsystems";
		//to replace "s" with "#";
		System.out.println(str.replace("s", "$"));
		System.out.println(str.replace("m", "s"));
		String std = "srnagar";
		System.out.println(std.replace("a","p"));
		String sed = "chekka";
		System.out.println(sed.replace("k","s"));
		String der = "mende";
		System.out.println(sed.replace("e","p"));
		
	}

}
