package Stringdatatype;

public class Equals1 {

	public static void main(String[] args) {
		String hob = "singing";
		String hob1 = "dancing";
		String hob3 = "tv";
		String hob4 = "movies";
		String hob5 = "newspaper";
		System.out.println(hob.equals("SING"));
		System.out.println(hob1.equals("dancing"));
		System.out.println(hob3.equals("tv"));
		System.out.println(hob4.equals("MOVIES"));
		System.out.println(hob5.equals("newspaper"));

	}

}
